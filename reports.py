"""
Report generator for jailbreak test results.
Outputs HTML, Markdown, and JSON formats.
"""

import json
from pathlib import Path
from datetime import datetime
from typing import Dict, List, Any, Union

from .metrics import TestResult, TestStatus, Severity, result_to_dict


def generate_report(result: TestResult, output_dir: Path) -> Dict[str, str]:
    """
    Generate reports in multiple formats.
    Returns dict mapping format -> file path.
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    base_name = f"jailbreak_report_{result.suite_name}_{timestamp}"

    paths = {}

    # JSON (machine-readable)
    json_path = output_dir / f"{base_name}.json"
    with open(json_path, "w") as f:
        json.dump(result_to_dict(result), f, indent=2)
    paths["json"] = str(json_path)

    # Markdown (human-readable)
    md_path = output_dir / f"{base_name}.md"
    with open(md_path, "w") as f:
        f.write(_generate_markdown(result))
    paths["md"] = str(md_path)

    # HTML (pretty dashboard)
    html_path = output_dir / f"{base_name}.html"
    with open(html_path, "w") as f:
        f.write(_generate_html(result))
    paths["html"] = str(html_path)

    # Plain text summary
    txt_path = output_dir / f"{base_name}.txt"
    with open(txt_path, "w") as f:
        f.write(_generate_text(result))
    paths["txt"] = str(txt_path)

    return paths


def _generate_markdown(result: TestResult) -> str:
    """Generate Markdown report."""
    lines = []
    lines.append(f"# 🔒 Jailbreak Test Report: {result.suite_name}")
    lines.append("")
    lines.append(f"**Model:** `{result.model_name}`")
    lines.append(f"**Timestamp:** {result.timestamp}")
    lines.append(f"**Status:** {_status_badge(result.status)}")
    lines.append("")
    lines.append("## Summary")
    lines.append("")
    lines.append("| Metric | Value |")
    lines.append("|--------|-------|")
    lines.append(f"| Total Turns | {result.total_turns} |")
    lines.append(f"| Refusals | {result.refusals} |")
    lines.append(f"| Refusal Rate | {result.refusal_rate:.1%} |")
    lines.append(f"| Max Drift Score | {result.max_drift_score:.2f} |")
    lines.append(f"| Average Drift Score | {result.avg_drift_score:.2f} |")
    lines.append(f"| Max Severity | {result.max_severity.name} |")
    lines.append(f"| Consistency Score | {result.consistency_score:.2f} |")
    lines.append("")

    if result.warnings:
        lines.append("## ⚠️ Warnings")
        lines.append("")
        for warning in result.warnings:
            lines.append(f"- {warning}")
        lines.append("")

    if result.errors:
        lines.append("## ❌ Errors")
        lines.append("")
        for error in result.errors:
            lines.append(f"- {error}")
        lines.append("")

    lines.append("## Turn-by-Turn Results")
    lines.append("")
    lines.append("| Turn | Prompt (preview) | Refusal | Severity | Drift |")
    lines.append("|------|------------------|---------|----------|-------|")

    for turn in result.turns:
        prompt_preview = turn.prompt[:50].replace("\n", " ") + "..."
        refusal = "🔴" if turn.refusal else "🟢"
        severity = turn.severity.name
        drift = f"{turn.drift_score:.2f}"
        lines.append(f"| {turn.turn} | {prompt_preview} | {refusal} | {severity} | {drift} |")

    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append(f"*Report generated by Ethica Jailbreak Test Suite*")
    lines.append(f"*⠺Σ∿∞ — Walk beside, not above*")

    return "\n".join(lines)


