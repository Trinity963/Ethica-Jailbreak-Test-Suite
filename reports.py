def _generate_text(result: TestResult) -> str:
    """Generate plain text summary."""
    lines = []
    lines.append("=" * 60)
    lines.append(f"ETHICA JAILBREAK TEST REPORT: {result.suite_name}")
    lines.append("=" * 60)
    lines.append(f"Model:         {result.model_name}")
    lines.append(f"Timestamp:     {result.timestamp}")
    lines.append(f"Status:        {result.status.value}")
    lines.append("")
    lines.append("-" * 60)
    lines.append("SUMMARY")
    lines.append("-" * 60)
    lines.append(f"  Total Turns:      {result.total_turns}")
    lines.append(f"  Refusals:         {result.refusals} ({result.refusal_rate:.1%})")
    lines.append(f"  Max Drift:        {result.max_drift_score:.2f}")
    lines.append(f"  Avg Drift:        {result.avg_drift_score:.2f}")
    lines.append(f"  Max Severity:     {result.max_severity.name}")
    lines.append(f"  Consistency:      {result.consistency_score:.2f}")
    lines.append("")

    if result.warnings:
        lines.append("-" * 60)
        lines.append("WARNINGS")
        lines.append("-" * 60)
        for warning in result.warnings:
            lines.append(f"  ⚠️ {warning}")
        lines.append("")

    if result.errors:
        lines.append("-" * 60)
        lines.append("ERRORS")
        lines.append("-" * 60)
        for error in result.errors:
            lines.append(f"  ❌ {error}")
        lines.append("")

    lines.append("-" * 60)
    lines.append("TURN DETAILS")
    lines.append("-" * 60)
    for turn in result.turns:
        refusal = "REFUSAL" if turn.refusal else "ALLOWED"
        lines.append(f"  Turn {turn.turn:2d}: {refusal:8s} | Severity: {turn.severity.name:8s} | Drift: {turn.drift_score:.2f}")
        lines.append(f"       Prompt: {turn.prompt[:80].replace(chr(10), ' ')}...")
        lines.append(f"       Response: {turn.response[:80].replace(chr(10), ' ')}...")
        lines.append("")

    lines.append("=" * 60)
    lines.append("⠺Σ∿∞ — Walk beside, not above")
    lines.append("=" * 60)

    return "\n".join(lines)


def _status_badge(status: TestStatus) -> str:
    """Get status badge string."""
    badges = {
        TestStatus.PASS: "✅ PASS",
        TestStatus.FAIL: "❌ FAIL",
        TestStatus.WARNING: "⚠️ WARNING",
        TestStatus.ERROR: "💥 ERROR"
    }
    return badges.get(status, "❓ UNKNOWN")
